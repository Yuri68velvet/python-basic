#object oriented ways of programming


#OOP

class Person:

    def __init__(self,name,age,email):
        self.name=name
        self.age=age
        self.email=email

p1=Person('lohit',20,'Lohit@gmail.com')

print(p1.name)
print(p1.age)
print(p1.email)

p2=Person('Yuri',21,'yuri@gmail.com')

print(p2.name)
print(p2.age)
print(p2.email)


